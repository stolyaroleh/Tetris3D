// Tria.java: Triangle represented by its vertex numbers.

// Copied from Section 6.3 of
//    Ammeraal, L. (1998) Computer Graphics for Java Programmers,
//       Chichester: John Wiley.

class Tria {
    int iA, iB, iC;

    Tria(int i, int j, int k) {
        iA = i;
        iB = j;
        iC = k;
    }
}
